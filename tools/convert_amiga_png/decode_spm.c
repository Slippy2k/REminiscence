static void blit_spm_depth3(int w, int h, unsigned char *data) {
	int y, x, i, bit, color, mask, offset = 0;
	int planar_size = w * 2 * h;

	for (y = 0; y < h; ++y) {
		for (x = 0; x < w; ++x) {
			for (i = 0; i < 16; ++i) {
				color = 0;
				mask = 1 << (15 - i);
				for (bit = 0; bit < 3; ++bit) {
					if (movew(data + bit * planar_size) & mask) {
						color |= 1 << bit;
					}
				}
				spmData16[offset] = color;
				++offset;
			}
			data += 2;
		}
	}
}

